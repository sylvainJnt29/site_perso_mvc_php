    <h3>Ecole Roger Bazille</h3>
    <p>Au cours de mon projet de fin de formation, j'ai été amené a créer un site web fictif d'une école primaire.</p>
        <br>
            Il devait permettre aux différents acteurs de l'école de pouvoir interagir entre eux ( professeurs, personnel de la mairie, parents d'élèves ... ).
            J'ai utilisé Symfony pour le faire, cela a été l'occasion pour moi de manipuler ce framework pour la première fois.
            J'ai appris beaucoup de notions grâce a ce dernier (<b>Bootstrap</b>, <b>twig</b>, <b>CRUD</b>, <b>administration</b>, <b>gestion de questions/réponses de façon dynamique</b> , <b>formulaire de contact</b>  ... )
